package mvc.model;

public class privateBookDTO {

	private int num;
	private String id;
	private String name;
	private String publisher;
	private String author;
	private String serial;
	private String location;
	private int vol;
	private String state;
	private String registday;
	private String lateRegistday;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getSerial() {
		return serial;
	}
	public void setSerial(String serial) {
		this.serial = serial;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public int getVol() {
		return vol;
	}
	public void setVol(int vol) {
		this.vol = vol;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getRegistday() {
		return registday;
	}
	public void setRegistday(String registday) {
		this.registday = registday;
	}
	public String getLateRegistday() {
		return lateRegistday;
	}
	public void setLateRegistday(String lateRegistday) {
		this.lateRegistday = lateRegistday;
	}
}
